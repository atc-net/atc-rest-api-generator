namespace Atc.Rest.ApiGenerator.Framework.ContentGenerators;

public class GeneratedCodeHeaderGenerator : IContentGenerator
{
    private readonly GeneratedCodeGeneratorParameters parameters;

    public GeneratedCodeHeaderGenerator(
        GeneratedCodeGeneratorParameters parameters)
    {
        this.parameters = parameters;
    }

    public string Generate()
    {
        var sb = new StringBuilder();
        sb.AppendLine("//------------------------------------------------------------------------------");
        sb.AppendLine($"// This code was auto-generated by {ContentWriterConstants.ApiGeneratorName} {parameters.ApiGeneratorVersion}.");
        sb.AppendLine("//");
        sb.AppendLine("// Changes to this file may cause incorrect behavior and will be lost if");
        sb.AppendLine("// the code is regenerated.");
        sb.AppendLine("//------------------------------------------------------------------------------");
        return sb.ToString();
    }
}